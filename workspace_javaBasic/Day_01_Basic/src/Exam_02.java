
public class Exam_02 {
	public static void main(String[] args) {
		byte a = 127;
		short b = 32767;
		char c = 'A';			// 컴퓨터 입장에서는 문자도 숫자이고, 문자는 그림을 그리는 것.
								// 'A' = 65. ''로 묶여있는 것은 모두 숫자임 결국.
		int d = 1000000000; 	// 약 20억대 까지 허용
		long e = 10000000000L; 	// L을 꼭 붙여줘야 함.
		float f = 3.14F;		// F/f 을 붙여줘야 함.
		double g = 5.12;
		boolean i = true;
		
		String h = "Hello world";
				
	}
}
