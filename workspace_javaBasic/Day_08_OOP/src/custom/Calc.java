package custom;

public class Calc {
	public static int plus(int num1, int num2) {
		return num1 + num2;
	}
	public static int minus(int num1, int num2) {
		return num1 - num2;
	}
	public static int mpy(int num1, int num2) {
		return num1 * num2;
	}
	public static double divide(int num1, int num2) {
		return (double)num1/num2;
	}
}
